#include "jabberd.h"

void base_accept(void);
void base_connect(void);
void base_exec(void);
void base_file(void);
void base_format(void);
void base_to(void);
void base_stderr(void);
void base_stdout(void);
void base_dynamic(void);
void base_syslog(void);

/* load all base modules */
void static_init(void)
{
    base_accept();
    base_connect();
    base_exec();
    base_file();
    base_format();
    base_to();
    base_stderr();
    base_stdout();
    base_dynamic();
    base_syslog();
}

